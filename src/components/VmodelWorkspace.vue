<template>
  <div class="hello">
    <h2>Child Component</h2>
    <h3>Input</h3>

    <!-- Title Input -->
    <div class="text-form">
      <p>title: </p>
      <input
        type="text"
        class="text-input"
        :value="title"
        @input="changeTitle($event.target.value)">
      <p>{{ title }}</p>
    </div>
    <div class="text-form">

    <!-- SubTitle Input -->
      <p>subTitle: </p>
      <input
        type="text"
        class="text-input"
        :value="subTitle"
        @input="changeSubTitle($event.target.value)">
      <p>{{ subTitle }}</p>
    </div>

    <!-- Color Input -->
    <div class="text-form">
      <p>color: </p>
      <input
        type="color"
        :value="color"
        @input="changeColor($event.target.value)">
      <p>{{ color }}</p>
      </div>

    <!-- Date Input -->
    <div class="text-form">
      <p>date: </p>
      <input
        type="date"
        :value="date"
        @input="changeDate($event.target.value)">
    </div>

    <!-- Radio Input -->
    <div class="text-form">
      <p>radio: </p>
      <div v-for="(country, i) in countries" :key="i">
        <input
          v-model="radioModel"
          type="radio"
          name="radio"
          :id="country"
          :value="country"
          @input="changeRadioValue($event.target.value)" >
        <label :for="country">{{ country }}</label>
      </div>
      <p class="radio-value">{{ radioValue }}</p>
    </div>

    <!-- Checkbox Input -->
    <div class="text-form">
      <p>checkbox: </p>
      <div v-for="(tomato, i) in tomatoes" :key="i">
        <input
          v-model="checkboxModel"
          type="checkbox"
          name="checkbox"
          :id="tomato"
          :value="tomato"
          @change="changeCheckbox()" >
        <label :for="tomato">{{ tomato }}</label>
      </div>
    </div>

    <!-- File Inout -->
    <div class="text-form">
      <p>file: </p>
      <input type="file" @input="changeFile($event.target.files[0])">
    </div>

    <button type="submit" @click="displayAlert()">
      表示
    </button>
  </div>
</template>
<script>
import { defineComponent, ref,} from 'vue'
export default defineComponent ({
  name: 'VmodelWorkspace',
  props: {
    // TODO: 非同期で初期値を取得する
    title: String,
    subTitle: String,
    color: String,
    date: String,
    radioValue: String,
    checkbox: Array,
    file: Object,
  },
  emits: {
    'update:title': null,
    'update:subTitle': null,
    'update:color': null,
    'update:date': null,
    'update:radioValue': null,
    'update:checkbox': null,
    'update:file': null,
    'display-alert': ({title, subTitle}) => {
      if (title && subTitle) {
        return true
      } else {
        console.warn('必須項目を入力してください')
        return false
      }
    },
  },
  setup(props, { emit }) {
    // constants
    const countries = ['Japan', 'USA', 'China']
    const tomatoes = ['トマト', 'プチトマト', 'ホールトマト缶', 'カットトマト缶']

    // refs
    const checkboxModel = ref(props.checkbox)
    const radioModel = ref(props.radioValue)

    // emits
    const changeTitle = (title) => {
      emit('update:title', title)
    }
    const changeSubTitle = (subTitle) => {
      emit('update:subTitle', subTitle)
    }
    const changeColor = (color) => {
      emit('update:color', color)
    }
    const changeDate = (date) => {
      emit('update:date', date)
    }
    const changeRadioValue = (value) => {
      emit('update:radioValue', value)
    }
    const changeCheckbox = () => {
      emit('update:checkbox', checkboxModel.value)
    }
    const changeFile = (file) => {
      emit('update:file', file)
    }
    const displayAlert = () => {
      emit('display-alert', { title: props.title, subTitle: props.subTitle })
    }

    return {
      countries,
      tomatoes,
      checkboxModel,
      radioModel,
      changeTitle,
      changeSubTitle,
      changeColor,
      changeDate,
      changeRadioValue,
      changeCheckbox,
      changeFile,
      displayAlert,
    }
  },
})
</script>

